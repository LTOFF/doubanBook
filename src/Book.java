
/*
 *书本类
 */
public class Book {
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    private String id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String name;

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    private double score;

    public String getCommetNum() {
        return commetNum;
    }

    public void setCommetNum(String commetNum) {
        this.commetNum = commetNum;
    }

    private String commetNum;

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPublicationData() {

        return publicationData;
    }

    public void setPublicationData(String publicationData) {
        this.publicationData = publicationData;
    }

    public String getPress() {

        return press;
    }

    public void setPress(String press) {
        this.press = press;
    }

    public String getAuthor() {

        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    private String author;
    private String press;
    private String publicationData;
    private String price;


}

